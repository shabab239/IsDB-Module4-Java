public class MathClass {
    
    public static void main(String[] args) {
        System.out.println(Math.round(2.8)); //returns int
        System.out.println(Math.rint(2.8)); //returns double
        System.out.println(Math.ceil(2.8));
        System.out.println(Math.floor(2.8));
        System.out.println(Math.abs(-2.8));
        System.out.println(Math.random());
        System.out.println(Math.min(50, 20));
        System.out.println(Math.max(50, 20));
        System.out.println(Math.pow(5, 3));
        System.out.println((int)(Math.random() * 100));
        System.out.println(Math.round((Math.random() * 20)));
    }
    
}
